package com.jkx.yang.bean;



public class MovieInfo {
	private String titles;//电影标题
	private String img_src;//电影图片路径
	private String movie_src;//电影路径
	private String movie_description;//电影描述
	private String movie_type;//电影类型
	public String getTitles() {
		return titles;
	}
	public void setTitles(String titles) {
		this.titles = titles;
	}
	public String getImg_src() {
		return img_src;
	}
	public void setImg_src(String imgSrc) {
		img_src = imgSrc;
	}
	public String getMovie_src() {
		return movie_src;
	}
	public void setMovie_src(String movieSrc) {
		movie_src = movieSrc;
	}
	public String getMovie_description() {
		return movie_description;
	}
	public void setMovie_description(String movieDescription) {
		movie_description = movieDescription;
	}
	public String getMovie_type() {
		return movie_type;
	}
	public void setMovie_type(String movieType) {
		movie_type = movieType;
	}
	
	
	
}
